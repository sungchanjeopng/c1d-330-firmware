I08* STR_Num2Str(S64 numb, U32 digit, U08 decimal, U08 f_fill)
{
	U08 i;
    U08 dg_num;
	U64 pow = 1;

	for(i=0; i<(digit-1); i++)
		pow *= 10;

	if  (decimal == 0)		dg_num = digit;
	else					dg_num = digit + 1;

	for(i=0; i<dg_num; i++)
	{
		if(i == (digit - decimal))
		{
			lStr_buff[i] = '.';
		}
		else
		{
			if(numb < pow)
			{
				if(f_fill == TRUE)
					lStr_buff[i] = '0';
				else
				{
					if(i >= (digit - decimal - 1))		lStr_buff[i] = '0';
					else								lStr_buff[i] = ' ';
				}
			}
			else
			{
				lStr_buff[i] = (numb / pow) % 10 + '0';
			}

			pow /= 10;
		}
	}
	lStr_buff[i] = 0;

	return lStr_buff;
}

