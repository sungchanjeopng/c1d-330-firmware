// Get
U16 MTH_ConvAtoB(U32 a, U32 a_min, U32 a_max, U32 b_min, U32 b_max)
{
	U16 val = 0;
	U32 a_rng = a_max - a_min;
	U32 b_rng = b_max - b_min;

	val = b_min + ( ((a-a_min) * b_rng) / a_rng );
	
	return val;
}

