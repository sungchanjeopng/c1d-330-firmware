void LED_InitVari(void)
{
	lLed_f_l01 = _F_F;

}

void LED_ProcMain(void)
{
	if(lLed_f_l01)		GIO_WrLed(GIO_LV_LO);
	else				GIO_WrLed(GIO_LV_HI);

	if(lLed_f_l01)		lLed_f_l01 = _F_F;
	else				lLed_f_l01 = _F_T;
}


