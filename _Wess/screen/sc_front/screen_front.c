U08 ScFRT_GetLayer(void)			{	return lScFrt.lyr;					}
void ScFRT_SetLayer(U08 lyr)		{	lScFrt.lyr = lyr;					}



//------------------------------------------------------------------------------------------------------------------------------
//  Global APIs - Basic Operation
//------------------------------------------------------------------------------------------------------------------------------
void ScFRT_InitMain(void)
{
	lScFrt.lyr = SCRN_L0_FUNC;

	ScFR0_InitFunc();
	ScFR1_InitList();
	ScFR2_InitValu();

}

void ScFRT_ProcMain(void)
{
	switch(lScFrt.lyr)
	{
		case SCRN_L0_FUNC:		ScFR0_ProcFunc();			break;
		case SCRN_L1_LIST:		ScFR1_ProcList();			break;
		case SCRN_L2_VALU:		ScFR2_ProcValu();			break;
	}
}


