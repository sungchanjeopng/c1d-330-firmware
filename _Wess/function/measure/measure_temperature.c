void MsTPR_InitVari(void)
{
	U08 i;
	
	gTp_now[0] = 0;
	gTp_now[1] = 0;
	lTp_cnt[0] = 0;
	lTp_cnt[1] = 0;

	for(i=0; i<4; i++)
	{
		lTp_buff[0][i] = -286;
		lTp_buff[1][i] = -286;
	}
}

void MsTPR_ProcCalc(U08 ch)
{
	U08 i;
	F32 sum = 0;
	F32 dat[2];

	sum = 0;
	dat[0] = ADC_GetTprtVal(ch);
	dat[1] = (-29.474 * log(dat[0])) + 196.03;

	lTp_buff[ch][lTp_cnt[ch]] = dat[1] * 10;
	lTp_cnt[ch] = (lTp_cnt[ch] + 1) % 4;

	for(i=0; i<4; i++) 
	    sum += lTp_buff[ch][i];

	gTp_deg[ch] = sum / 4;

	gTp_now[ch] = gTp_deg[ch];

	CalPrc_Tprt_Check(ch);

}


